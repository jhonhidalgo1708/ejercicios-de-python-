#area y perimetro de un rectangular
#inicio de los datos

base=float(input("Insertar la base rectangular: "))
altura=float(input("Insertar la altura del 10 rectangulo: "))

#operacion 
area=(base*altura)
perimetro=2*(base+altura)
#salida
print(f"El área es: {area} cm²")
print(f"El perímetro es: {perimetro} cm²")